package com.kb.adapterdemo.base_adapters;

public class CountryDataModel {
    String name;
    int code;

    public CountryDataModel(String name, int code) {
        this.name = name;
        this.code = code;
    }

    public String getName() {
        return name;
    }

    public int getCode() {
        return code;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setCode(int code) {
        this.code = code;
    }
}
